<h3>Пампорово</h3>
Пампо̀рово е планински курорт в България. Разположен е във вътрешността на Родопите на 1650 м надморска височина, в подножието на връх Снежанка. Намира се на територията на Община Смолян и на Община Чепеларе, на разстояние около 220 км от София, 85 км южно от Пловдив, 15 км северно от Смолян и 10 км южно от Чепеларе.
