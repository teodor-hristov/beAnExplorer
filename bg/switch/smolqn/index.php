<h3>Дяволското гърло</h3>
Дяволското гърло е пещера в Родопите, община Девин. Пещерата е разположена на 1,5 km северно от село Триград в Триградското ждрело в Родопите. Името на пещерата идва от формата на бившия вход (сега – изход), наподобяващ дяволска глава.
