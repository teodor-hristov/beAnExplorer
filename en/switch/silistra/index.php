<h3>Архитектурен резерват "Рибарското селище"</h3>
В Тутракан е единственото в страната естествено обособило се по поречието на реката рибарско селище. Със своята самобитност, чистота и възрожденски дух Архитектурен комплекс "Рибарската махала" е съхранен за поколенията в своя автентичен вид с рибарските мрежи, с лодкарските работилници и рибарските къщи.
