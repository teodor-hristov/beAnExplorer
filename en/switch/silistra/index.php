<h3>Етнографски музей „Дунавски риболов и лодкостроене“</h3>
Етнографски музей “Дунавски риболов и лодкостроене” в Тутракан е част от Историческия музей на града. Той е открит през 1974 г. и е единствен в страните по поречието на река Дунав. Създаден е, за да съхрани богатата материална и духовна култура, обществено устройство и бит на българите от крайдунавските рибарски селища.
