<h3>Художествена галерия "Жорж Папазов"</h3>
Художествена галерия „ Жорж Папазов” – Ямбол води началото си от 1952 г. като художествен отдел към Исторически музей. Сградата на бившата Синагога е обявена за паметник на културата от 18.11.1967 г. и адаптирана за художествена галерия през 1979 г.
