<h3>Исторически музей</h3>
Историческият музей в Ямбол се явява наследник на създаваните през годините музейни сбирки в града. Първата сбирка датира още от 1886г. и е разкрита в трикласното мъжко училище в града. Музеят е открит официално на 2.6.1953 г. като "Музей на революционното движение".
