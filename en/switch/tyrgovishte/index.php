<h3>Регионален исторически музей</h3>
Регионалният исторически музей в Търговище е основан през 1951 година. Той e наследник на създадените през 20-те години на 20 век музейни сбирки. Понастоящем той е общински музей. Извършва проучвания на територията на Област Търговище. Решението за създаване на музей датира от 1951 година. Започва се на обществени начала. От 1953 година има постоянна музейна експозиция. Окръжният исторически музей — Търговище функционира от 1960 година като самостоятелен институт, който системно проучва и популяризира историята на Търговищкия край.
