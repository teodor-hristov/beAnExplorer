<h3>Хаджиангелова къща</h3>
Хаджиангеловата къща-музей e български архитектурен паметник на културата от XIX век. Намира се в град Търговище, квартал Вароша, на адрес ул. "Раковски" № 14.
Построена през 1863 г. от тревненски майстори, сградата е образец на симетрична възрожденска къща с дърворезбена украса, художествено изписани холкери и модерни за времето си архитектурни детайли – алафранга, зидана печка и други.В нея е живял българския търговец Хаджи Ангел Хаджи Друмев, в нея могат да се разгледат инструменти, използвани в лозарството, грънчарството, земеделието и други.
